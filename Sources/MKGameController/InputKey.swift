import Foundation

/// Keys supported by the D-Pad touch countroller
public enum InputKey: String {
    
    /// direction key - up
    case keyUp
    
    /// direciton key - down
    case keyDown
    
    /// direction - left
    case keyLeft
    
    /// direction key - right
    case keyRight
    
    /// action key- X
    case keyX
    
    /// action key - Y
    case keyY
    
    /// action key - A
    case keyA
    
    /// action key - B
    case keyB
}
